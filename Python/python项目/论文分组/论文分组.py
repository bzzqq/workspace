import os
import shutil


def move_files_with_keywords(source_folder, destination_folder, keywords):
    # 遍历源文件夹中的文件
    for root, dirs, files in os.walk(source_folder):
        for dir in dirs:
            # 检查文件名是否包含关键字
            if any(keyword in dir for keyword in keywords):
                # 构建源文件路径和目标文件路径
                source_path = os.path.join(root, dir)
                destination_path = os.path.join(destination_folder, dir)
                # 移动文件到目标文件夹
                shutil.move(source_path, destination_path)
                print(f"Moved file: {dir}")


def delete_files_with_keywords(source_folder, keywords):
    # 遍历源文件夹中的文件
    for root, dirs, files in os.walk(source_folder):
        for file in files:
            # 检查文件名是否包含关键字
            if any(keyword in file for keyword in keywords):
                # 删除文件
                file_path = os.path.join(root, file)
                os.remove(file_path)
                print(f"Moved file: {file}")


